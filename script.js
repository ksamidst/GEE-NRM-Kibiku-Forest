//Define the input bands for classification
var bands = ['B2', 'B3', 'B4', 'B5', 'B6', 'B7', 'B8', 'B11', 'B12'];

/* Below training features are picked using the geometry drawing tools in the map display
    shown below the code editor. They are drawn around the identified land cover on the source image,
    and labelled using the numeric labels shown above, then merged into the training features*/
var training_points = (Natural_Forest_2020).merge(Plantion_Forest_2020).merge(Bareland_2020).merge(cropland_2020).merge(Grassland_2020);
print('#2020_Training points', training_points);

//introduce a random column to hold a random number between 0 and 1.
 var sample = training_points.randomColumn();

//Divide the training features further into training and validation features, with 0.7 as the mid-point
 var split = 0.7;
 
 //Take the training set by selecting the features with a value less than 0.7
 var trainingSP = sample.filter(ee.Filter.lt('random', split));
 print('Training points for Classification', trainingSP )

//Sample the training data (sample data) from the training set
var training2020 = Image2020.select(bands).sampleRegions({
  collection: trainingSP,
  properties: ['Landcover'],
  scale: 10
});

/* Initialize and train a random forest classifier 'smileRandomForest' using the 'train()' function.
    the arguments for the train function are as follows in order:
    1. The sample data
    2. The name of the property of the training features containing the label for classification
    3. The classification input bands
    */
var trained2020 = ee.Classifier.smileRandomForest(10).train(training2020, 'Landcover', bands);

//Classify the cloud free image using the 'classify()' function and pass the trained classifier as input.
var classified2020 = Image2020.select(bands).classify(trained2020);

/*The classification process produces an image with a single band, the classification band. The image is divided into classified regions,
  whereby the pixels in each region have different values for the classification band. For each class, the image has pixel value
  corresponding to the classification value for that class.*/

//visualize the classified image.
Map.addLayer(classified2020,
{min: 1, max: 5, palette: ['006400','7fff00','808000','F0E68C','FF7F50']},'Classification2020');

