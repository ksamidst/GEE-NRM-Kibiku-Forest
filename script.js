//Define cloud masking function for Sentinel-2
function maskS2clouds(image){
  var qa = image.select('QA60');

  // Bits 10 and 11 are clouds and cirrus, respectively.
  var cloudBitMask = 1 << 10;
  var cirrusBitMask = 1 << 11;

  // Both flags should be set to zero, indicating clear conditions.
  var mask = qa.bitwiseAnd(cloudBitMask).eq(0)
      .and(qa.bitwiseAnd(cirrusBitMask).eq(0));
  return image.updateMask(mask).divide(10000);
}
var Image2020 = sentinel2
              //filter the image collection to the 2020 date range
              .filterDate('2020-01-01','2020-12-31')
              //retain the image meta-data for the Kibiku forest area
              .filterBounds(AOI)
              //apply the cloud masking function to the remaining images in the collection
              .map(maskS2clouds)
              // use the 'median()' reduction function to produce a single image from the collection
              .median().clip(AOI);
              
              

